/*
== igual a
=== igual restrito
!= não é igual
!== não é igual restrito
> maior que
< menor que
>= maior ou igual a
<= menor ou igual a
*/

// console.log(3 == 3);

var a = 3;
var b = 4;
console.log(a == b);

// Igual restrito
var a = "30";
var b = 30;
console.log(a === b);

// não igual
var c = 28;
var d = 28;

console.log(c != d);

// não igual restrito
var e = 90;
var f = "90";

console.log(e !== f);

// maior que
var g = 54;
var h = 24;

console.log(g > h);

// menor que
var i = 52;
var j = 21;

console.log(i < j);

// maior ou igual a
var k = 36;
var l = 85;

console.log(k >= l);

// menor ou igual a
var m = 92;
var n = 73;

console.log(m <= n);
